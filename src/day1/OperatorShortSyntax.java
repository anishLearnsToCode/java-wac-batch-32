package day1;

public class OperatorShortSyntax {
    /*
        Time Complexity: O(1)
     */
    public static void main(String[] args) {
        /*
            variabl1 = variabl1 [operation] variable2;
            variable1 [operator]= variable2;

            i = i + 1; --> i += 1; --> i++;
            sum = sum + number; --> sum += number;
            product = product * factor; --> product *= factor;
            d = d / 100; --> d /= 100;
         */

        int sum = 0;
        sum += 10;
    }
}
